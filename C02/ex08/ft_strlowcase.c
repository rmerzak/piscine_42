int	ft_is_ALFA(char *c)
{
	if (*c <= 'Z' && *c >= 'A')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_is_ALFA(&str[i]))
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
