int	ft_is_alfa(char *c)
{
	if (*c >= 'a' && *c <= 'z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_is_alfa(&str[i]))
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
